void main() {
  // +
  int num1 = 1;
  int num2 = 2;

  var result = num1 + num2;
  print(result);

  // -
  int num3 = 1;
  int num4 = 2;

  var result2 = num3 - num4;
  print(result2);

  // *
  int num5 = 1;
  int num6 = 2;

  var result3 = num5 * num6;
  print(result3);

  // /
  int num7 = 1;
  int num8 = 2;

  var result4 = num7 / num8;
  print(result4);


  // %
  int num9 = 1;
  int num0 = 2;

  var result5 = num9 % num0;
  print(result5);

}
